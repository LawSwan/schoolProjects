public class firstCalculation {

    public static void firstCalculation(int a, int b) {
        int result = a + b;  // Example: addition
        System.out.println("First Calculation (a + b): " + result);
    }

    public static void secondCalculation(int a, int b) {
        int result = a * b;  // Example: multiplication
        System.out.println("Second Calculation (a * b): " + result);
    }

    public static void main(String[] args) {
        // Example usage
        firstCalculation(10, 5);
        secondCalculation(10, 5);
    }
}